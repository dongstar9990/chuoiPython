    // Hàm click vào tên sinh viên trong bảng
    void selectionTable() {
// Tạo danh sách lựa chọn của người dùng tên bảng và thiết lập chỉ chọn 1.
        ListSelectionModel listselectionmodel = table.getSelectionModel();
        listselectionmodel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
// Thêm hàm lắng nghe sự kiện lựa chọn trên bảng
        listselectionmodel.addListSelectionListener(new ListSelectionListener() {
// Thêm hàm thay đổi giá trị lựa chọn
            @Override
            public void valueChanged(ListSelectionEvent e) {
// Lấy danh sách các cột và các hàng lựa chọn
                int[] rows = table.getSelectedRows();
                int[] colums = table.getSelectedColumns();
// Nếu có hàng được lựa chọn thì chạy tiếp
                if (colums.length > 0 && rows.length > 0) {
// Gán các giá trị từ bảng vào các ô TexField bên dưới
                    txtmaut.setText(String.valueOf(table.getValueAt(rows[0], 0)));
                    txtmaut.setEditable(false);
                    txttenut.setText(String.valueOf(table.getValueAt(rows[0], 1)));
                    txtgt.setText(String.valueOf(table.getValueAt(rows[0], 2)));
                    txtngsinh.setText(String.valueOf(table.getValueAt(rows[0], 3)));
                    txtsdt.setText(String.valueOf(table.getValueAt(rows[0], 4)));
                    txtemail.setText(String.valueOf(table.getValueAt(rows[0], 5)));
                    txtdiachi.setText(String.valueOf(table.getValueAt(rows[0], 6)));
                    txttrinhdo.setText(String.valueOf(table.getValueAt(rows[0], 7)));
                    txtchuyennganh.setText(String.valueOf(table.getValueAt(rows[0], 8)));
                    txtxeploai.setText(String.valueOf(table.getValueAt(rows[0], 9)));
                    txtvitri.setText(String.valueOf(table.getValueAt(rows[0], 10)));
                }
            }
        });
    }

// Hàm hiển thị bảng
    void showTable() {
// Lấy danh sách nguoi tuyen dung tưf file TuyenDung.dat
        if (tuyendungdta.docFile() != null) {
            list = tuyendungdta.docFile();
        }
        //Đặt bảng về mặc định không cột không hàng
        DefaultTableModel dtm = new DefaultTableModel();
        table.setModel(dtm);
// Thêm tiêu đề cho bảng
        dtm.addColumn("Mã");
        dtm.addColumn("Tên");
        dtm.addColumn("Giới tính");
        dtm.addColumn("Ngày sinh");
        dtm.addColumn("Số điện thoại");
        dtm.addColumn("Email");
        dtm.addColumn("Địa chỉ");
        dtm.addColumn("Trình độ");
        dtm.addColumn("Chuyên ngành");
        dtm.addColumn("Xếp loại");
        dtm.addColumn("Vị trí ứng tuyển");
// Nếu danh sách khác null thì thực hiện tiếp
        if (list != null) {
            for (int i = 0; i < list.size(); i++) {
                TuyenDung a = list.get(i);
                dtm.addRow(new Object[]{a.getMa(), a.getTen(), a.getGtinh(), a.getNgsinh(), a.getSdt(), a.getEmail(), a.getDchi(),
                    a.getTrinhdo(), a.getCnganh(), a.getXeploai(), a.getVtungtuyen()});
            }
        }
    }

