    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        
       // Nếu ds TuyenDung rỗng thì khởi tạo mảng list = tuyendungdata.docFile();
        if (tuyendungdta.docFile() != null) {
            list = tuyendungdta.docFile();
        }
        //khai báo biến để gán giá trị bằng phương thức getText
        String ma = txtmaut.getText();
        String name = txttenut.getText();
        String gioitinh = txtgt.getText();
        String ngaysinh = txtngsinh.getText();
        String sdt = txtsdt.getText();
        String email = txtemail.getText();
        String diachi = txtdiachi.getText();
        String trinhdo = txttrinhdo.getText();
        String chuyennganh = txtchuyennganh.getText();
        String xeploai = txtxeploai.getText();
        String vitri = txtvitri.getText(); 
        // bắt lỗi khi nhập thiếu thông tin
        if (ma.equals("") || name.equals("") || gioitinh.equals("") || ngaysinh.equals("") || sdt.equals("") || email.equals("") 
                || diachi.equals("") || trinhdo.equals("") || chuyennganh.equals("") || xeploai.equals("") || vitri.equals("")) {
            JOptionPane.showMessageDialog(this, "Các trường không được bỏ trống");
        } else { 
            // kiểm tra mã nếu trùng ko cho thêm
            if (TuyenDung.check(ma) == 1) { 
                JOptionPane.showMessageDialog(this, "Ma tuyen dung da ton tai!");
            } else { // Ngược lại khi nhập đầy đủ các trường 
                TuyenDung a = new TuyenDung(ma, name, gioitinh, ngaysinh, sdt, email, diachi, trinhdo, chuyennganh, xeploai, vitri);
                // thì sẽ tạo ra 1 TuyenDung mới được ghi lại vào file và hiện lên màn hình
                list.add(a);
                tuyendungdta.ghiFile(list);  // ghi file vào ds
                txtmaut.setText("");
                txttenut.setText("");
                txtgt.setText("");
                txtngsinh.setText("");
                txtsdt.setText("");
                txtemail.setText("");
                txtdiachi.setText("");
                txttrinhdo.setText("");
                txtchuyennganh.setText("");
                txtxeploai.setText("");
                txtvitri.setText("");
                showTable();
                JOptionPane.showMessageDialog(this, "Thêm thành công"); // báo thêm thành công
            }
        }
    }//GEN-LAST:event_btthemActionPerformed

