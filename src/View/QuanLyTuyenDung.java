    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimkiemActionPerformed
        // TODO add your handling code here:
         int count = 0;
        for (int i = 0; i < table.getRowCount(); i++) {
            if (String.valueOf(table.getValueAt(i, 0)).equals(jtf_timkiem.getText()) || 
                    String.valueOf(table.getValueAt(i, 1)).equals(jtf_timkiem.getText())) {
                count++;
                DefaultTableModel dtm = new DefaultTableModel();
                table.setModel(dtm);
                // Thêm tiêu đề cho bảng
                dtm.addColumn("Mã");
                dtm.addColumn("Tên");
                dtm.addColumn("Giới tính");
                dtm.addColumn("Ngày sinh");
                dtm.addColumn("Số điện thoại");
                dtm.addColumn("Email");
                dtm.addColumn("Địa chỉ");
                dtm.addColumn("Trình độ");
                dtm.addColumn("Chuyên ngành");
                dtm.addColumn("Xếp loại");
                dtm.addColumn("Vị trí ứng tuyển");

                //insert into table
                TuyenDung a = list.get(i);
                dtm.addRow(new Object[]{a.getMa(), a.getTen(), a.getGtinh(), a.getNgsinh(), a.getSdt(), a.getEmail(), 
                    a.getDchi(), a.getTrinhdo(), a.getCnganh(), a.getXeploai(), a.getVtungtuyen()});
            }
        }
        if(count == 0){
            JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả", "Warning", JOptionPane.WARNING_MESSAGE);
            showTable();
        }
    }//GEN-LAST:event_btnTimkiemActionPerformed


